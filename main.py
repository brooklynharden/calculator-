#Addition Function
def add(num1, num2):
    sum = num1 + num2
    return sum

#Subtraction Function
def subtract(num1, num2):
    sum = num1 - num2
    return sum


#Division Function
def divide(num1, num2):
    sum = num1 / num2
    return sum


#Modulo Function
def mod(num1, num2):
    sum = num1%num2
    return sum

#Multiplication Function
def multiply(num1, num2):
    sum = num1*num2
    return sum

